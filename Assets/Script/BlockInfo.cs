using UnityEngine;
using System.Collections;
using SimpleJSON;

public class BlockInfo
{
    public int posX;
    public int posY;
    public int posZ;
    public BLOCK_TYPE type;
    public float rotation = 0.0f;

    public BlockInfo(int posX, int posY, int posZ, BLOCK_TYPE type)
    {
        this.posX = posX;
        this.posY = posY;
        this.posZ = posZ;
        this.type = type;
    }

    public JSONNode SaveToJSON()
    {
        JSONNode node = new JSONClass();
        node["posX"] = posX.ToString();
        node["posY"] = posY.ToString();
        node["posZ"] = posZ.ToString();
        node["type"] = type.ToString();
        node["rotation"] = rotation.ToString();
        return node;
    }
}